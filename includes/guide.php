  <h2>Holiday Countdown Documentation</h2>
  <h3>Adding a Countdown Clock</h3>
  <p>Adding a countdown clock to your website is really easy. All you need to do is&hellip;</p>
  <ol>
    <li>Go to widget settings under &quot;Appearance&quot; &rarr; &quot;Widgets&quot;</li>
    <li>Find the widget named &quot;Holiday Countdown&quot; on the left hand side of the page</li>
    <li>Drag the &quot;Holiday Countdown&quot; widget over to the area on the page you want it (located on the right hand side)</li>
    <li>Choose the holiday you want to countdown to in the widget settings and click &quot;Save&quot;</li>
  </ol>
  <p>Once you have all that done, just visit your site and see your new countdown clock in action!</p>
